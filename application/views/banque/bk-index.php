        $.get("<?= site_url('ajax/chart-data') ?>", {
          devise: devise
        }, function(d) {
          d = JSON.parse(d)
          var tab_data = [];
          $.each(d, function(i, j) {
            tab_data.push({
              name: i,
              data: j
            })
          })
          chart.updateSeries(tab_data)
        })
      }

      $('.devise').change(function() {
        chart_data($(this).val())
      })


